package javaprogrammeweek3;

public class Programme9Switch {
    public static void main(String[] args) {
        String inputAlphabet = "A";
        switch (inputAlphabet){
            case "A":
                System.out.println("City: Ahmedabad");
                break;
            case "B":
                System.out.println("City: Baroda");
                break;
            case "C":
                System.out.println("City: Coimbatore");
                break;
            case "D":
                System.out.println("City: Daman");
                break;
            case "E":
                System.out.println("City: East Baroda");
                break;
            case "F":
                System.out.println("City: Faizabad");
                break;
        }
    }
}
